// Собирает content/index.ts из content/vocab/*.json (только колоды слов).
// Ситуации и SOS убраны из продукта (BS-19) — продукт про слова.
// Порядок — по кураторскому списку ниже (частые сверху), остальное — по алфавиту.
// Запуск: node scripts/build-content-index.mjs
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const CONTENT = join(ROOT, 'content');

const DECK_ORDER = [
  'uctivost', 'glagoli', 'brojevi', 'vreme_sati', 'dani_meseci', 'pitanja_reci',
  'predlozi_veznici', 'novac_cene', 'namirnice', 'voce_povrce', 'meso_riba',
  'mleko_pekara', 'restoran_meni', 'prodavnice_usluge', 'odeca', 'boje', 'telo',
  'simptomi', 'apoteka_lekovi', 'lekar_bolnica', 'dokumenti', 'stan_kuca', 'namestaj',
  'kuhinja', 'kupatilo', 'ciscenje', 'alat_popravke', 'prevoz', 'auto_voznja',
  'grad_orijentacija', 'vreme_klima', 'banka_finansije', 'telefon_internet',
  'posao_kancelarija', 'porodica_ljudi', 'emocije_pridevi', 'zivotinje_priroda',
  'skola_obrazovanje', 'lazni_prijatelji',
];

function collect(subdir, order) {
  let files = [];
  try {
    files = readdirSync(join(CONTENT, subdir)).filter((f) => f.endsWith('.json'));
  } catch {
    return [];
  }
  const items = files.map((f) => {
    const id = JSON.parse(readFileSync(join(CONTENT, subdir, f), 'utf8')).id;
    return { id, file: f };
  });
  const rank = (id) => {
    const i = order.indexOf(id);
    return i === -1 ? order.length : i;
  };
  items.sort((a, b) => rank(a.id) - rank(b.id) || a.id.localeCompare(b.id));
  return items;
}

const decks = collect('vocab', DECK_ORDER);

const lines = [];
lines.push('// AUTO-GENERATED by scripts/build-content-index.mjs — не редактировать руками.');
lines.push("import type { RawDeck } from '@/lib/types';");
lines.push('');
decks.forEach((d, i) => lines.push(`import vo${i} from './vocab/${d.file}';`));
lines.push('');
lines.push(`export const decks = [${decks.map((_, i) => `vo${i}`).join(', ')}] as unknown as RawDeck[];`);
lines.push('');

writeFileSync(join(CONTENT, 'index.ts'), lines.join('\n'), 'utf8');
console.log(`content/index.ts: ${decks.length} колод.`);
